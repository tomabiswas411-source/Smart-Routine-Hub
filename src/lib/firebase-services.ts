// Helper to convert Firestore doc to typed object
function docToObj<T>(doc: { id: string; data: () => Record<string, unknown> }): T {
  const data = doc.data();
  return { id: doc.id, ...data } as T;
}

// ============ USERS ============
export async function getUser(id: string): Promise<User | null> {
  try {
    const docRef = doc(db, "users", id);
    const docSnap = await getDoc(docRef);
    if (!docSnap.exists()) return null;
    return docToObj<User>(docSnap as unknown as { id: string; data: () => Record<string, unknown> });
  } catch (error) {
    console.error("Error getting user:", error);
    return null;
  }
}

export async function getUserByEmail(email: string): Promise<User | null> {
  try {
    const q = query(collection(db, "users"), where("email", "==", email), limit(1));
    const querySnapshot = await getDocs(q);
    if (querySnapshot.empty) return null;
    const docSnap = querySnapshot.docs[0];
    return docToObj<User>(docSnap as unknown as { id: string; data: () => Record<string, unknown> });
  } catch (error) {
    console.error("Error getting user by email:", error);
    return null;
  }
}

export async function getUsers(role?: string): Promise<User[]> {
  try {
    // Simple query without compound where clauses
    const q = query(collection(db, "users"), where("isActive", "==", true));
    const querySnapshot = await getDocs(q);
    let users = querySnapshot.docs.map((doc) => docToObj<User>(doc as unknown as { id: string; data: () => Record<string, unknown> }));
    
    // Filter in memory
    if (role) {
      users = users.filter(u => u.role === role);
    }
    
    // Sort in memory
    return users.sort((a, b) => a.fullName.localeCompare(b.fullName));
  } catch (error) {
    console.error("Error getting users:", error);
    return [];
  }
}

