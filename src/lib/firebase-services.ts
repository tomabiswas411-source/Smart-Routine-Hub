// ============ SCHEDULES ============
export async function getSchedules(filters?: {
  year?: number;
  semester?: number;
  program?: string;
  day?: string;
  teacherId?: string;
}): Promise<Schedule[]> {
  try {
    // Use simple query - only one where clause to avoid index requirements
    const q = query(collection(db, "schedules"), where("isActive", "==", true));
    const querySnapshot = await getDocs(q);
    let schedules = querySnapshot.docs.map((doc) => docToObj<Schedule>(doc as unknown as { id: string; data: () => Record<string, unknown> }));
    
    // Filter in memory
    if (filters?.year) {
      schedules = schedules.filter(s => s.year === filters.year);
    }
    if (filters?.semester) {
      schedules = schedules.filter(s => s.semester === filters.semester);
    }
    if (filters?.program) {
      schedules = schedules.filter(s => s.program === filters.program);
    }
    if (filters?.day) {
      schedules = schedules.filter(s => s.dayOfWeek === filters.day);
    }
    if (filters?.teacherId) {
      schedules = schedules.filter(s => s.teacherId === filters.teacherId);
    }
    
    return schedules;
  } catch (error) {
    console.error("Error getting schedules:", error);
    return [];
  }
}

