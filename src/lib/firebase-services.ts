// ============ SCHEDULE CHANGES ============
export async function getScheduleChanges(filters?: {
  teacherId?: string;
  effectiveDate?: string;
}): Promise<ScheduleChange[]> {
  try {
    const constraints = [where("isActive", "==", true)];

    if (filters?.teacherId) {
      constraints.push(where("teacherId", "==", filters.teacherId));
    }
    if (filters?.effectiveDate) {
      constraints.push(where("effectiveDate", "==", filters.effectiveDate));
    }

    const q = query(collection(db, "scheduleChanges"), ...constraints);
    const querySnapshot = await getDocs(q);
    const changes = querySnapshot.docs.map((doc) => docToObj<ScheduleChange>(doc as unknown as { id: string; data: () => Record<string, unknown> }));
    // Sort by createdAt descending
    return changes.sort((a, b) => {
      const aTime = a.createdAt ? new Date(a.createdAt as unknown as string).getTime() : 0;
      const bTime = b.createdAt ? new Date(b.createdAt as unknown as string).getTime() : 0;
      return bTime - aTime;
    });
  } catch (error) {
    console.error("Error getting schedule changes:", error);
    return [];
  }
}

